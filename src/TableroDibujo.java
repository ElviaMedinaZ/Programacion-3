	 // controla
    public void controlarMusica() {
        if (musicaActivada) {
            detenerMusica();
            musicaActivada = false;
            frame.requestFocus();  
        } else {
            iniciarMusica();
            musicaActivada = true;
            frame.requestFocus();  
        }
        cambiabtnMusica();
    }

    // inicia el cumbion
    public void iniciarMusica() {
        try {
            AudioInputStream audioInputStream = AudioSystem.getAudioInputStream(new File("src/laberinto.wav").getAbsoluteFile());
            clip = AudioSystem.getClip();
            clip.open(audioInputStream);
            clip.start();
        } catch (Exception ex) {
            System.out.println("Error xd");
            ex.printStackTrace();
        }
    }

    //detiene la musiquita
    public void detenerMusica() {
        if (clip != null && clip.isRunning()) {
            clip.stop();
            
        }
    }
    
    public void cambiabtnMusica() {
        if (musicaActivada) {
            btnMusica.setText("    Musica activada ♫   "); 
        } else {
            btnMusica.setText("Musica desactivada ♫"); 
        }
    }
    
    ///////////////////////////////////////////////////// TIEMPO /////////////////////////////////////////////////////////////
    public void tiempo() {
        miliseg++;
        
        if(miliseg == 100) {
            miliseg = 0;
            seg++;
            
            if(seg == 60) {
                seg = 0;
                min++;
                
                if(min == 60) {
                    min = 0;
                    hrs++;
                    
                    if(hrs == 24) {
                        hrs = 0;
                    }
                }
            }
        }
        
        correrTiempo();
    }
    
	
	
