    private void update() {
    	
    	 switch (direccion) {
         case 87: // w
             movX = 0;
             movY = -1;
             break;
         case 83: // s
             movX = 0;
             movY = 1;
             break;
         case 68: // d
             movX = 1;
             movY = 0;
             break;
         case 65: // a
             movX = -1;
             movY = 0;
             break;
         default:
             movX = 0;
             movY = 0;
             break;
 
        }
    	 
    	 int posViejaX = p1.getX();
    	 int nuevaX = p1.getX() + movX * 10;
    	 int nuevaY = p1.getY() + movY * 10;
    	    
    	    if (tablero[nuevaY/10][nuevaX / 10] == 2) { 
    	       timer.stop();
               JOptionPane.showMessageDialog(frame, "Felidades vete por un cafecito :3\nTiempo: "+hrs+":"+min+":"+seg, "Ganaste", JOptionPane.WARNING_MESSAGE);
               movimiento.stop(); 
               frame.repaint();
               p1.setX(posViejaX);

    	        movimiento.stop(); // Detiene el movimiento
    	    } else if (tablero[nuevaY/10][nuevaX / 10] != 1) { // Si no hay un obstáculo, avanza
    	        p1.setX(nuevaX);
    	        p1.setY(nuevaY);
    	        System.out.println("Avanzando");
    	    } else {
    	        System.out.println("Colision"); // Si hay un obstáculo, muestra un mensaje de colisión
    	    }

    }
    
